#get the fahrenheit value from user
#pega o valor fahrenheit do usurário
fahrenheit = float(input("digite o falor em fahrenheit: "))
#perform the calculus to convert
#realiza o calculo para converter
celsius = 5 * ((fahrenheit - 32) / 9)
#Show the result of a convertion
#Mostra o resultado da conversão
print("o valor da conversão para graus celsius é:",celsius)